    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if(jId.getText().equals("")){
            JOptionPane.showMessageDialog(null,"ID harus diisi","Informasi",JOptionPane.ERROR_MESSAGE);
        }else if(jNama.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Nama harus diisi","Informasi",JOptionPane.ERROR_MESSAGE);
        }else if(jEmail.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Email harus diisi","Informasi",JOptionPane.ERROR_MESSAGE);
        }else if(jPassword.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Password harus diisi","Informasi",JOptionPane.ERROR_MESSAGE);
        }else if(jNope.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Nomor HP harus diisi","Informasi",JOptionPane.ERROR_MESSAGE);
        }else{
            int opsi = JOptionPane.showConfirmDialog(null,"Simpan data?","KOnfirmasi",JOptionPane.YES_NO_OPTION);
            if(opsi == JOptionPane.YES_OPTION){
                tambah_data();
                reset_form();
                IDotomatis();
            }else{
                JOptionPane.showMessageDialog(null,"Data tidak tersimpan","Informasi",JOptionPane.ERROR_MESSAGE);
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanActionPerformed

