    private void IDotomatis(){
        try{
            stm=koneksi.con.createStatement();
            String sql = "SELECT * FROM admin order by id desc";
            rs=stm.executeQuery(sql);
            if(rs.next()){
                String ID = rs.getString("id").substring(5);
                String NO =""+(Integer.parseInt(ID)+1);
                String No1="";
                
                if(NO.length()==1){
                    No1="00";
                }else if(NO.length()==2){
                    No1="0";
                }else if(NO.length()==3){
                    No1="";
                }
                jId.setText("ADMIN" + No1 + NO);
            }else{
                jId.setText("ADMIN001");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Id otomatis error"+e);
        }
    }
    
    public void tambah_data(){
        try{
            stm=koneksi.con.createStatement();
            String sql="INSERT INTO admin values('"+jId.getText()+
                    "','"+jNama.getText()
                    +"','"+jEmail.getText()
                    +"','"+jPassword.getText()
                    +"','"+jNope.getText()
                    +"')";
            stm.execute(sql);
            JOptionPane.showMessageDialog(null,"Data Berhasil dimasukkan");
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Tambah data ERROR!!"+e);
        }
        
    }
    
