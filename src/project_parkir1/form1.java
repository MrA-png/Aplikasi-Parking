    public void kelas(){
        try{
            stm=koneksi.con.createStatement();
            String query = "SELECT * FROM kelas";
            rs=stm.executeQuery(query);
            while(rs.next()){
                jKelas.addItem(rs.getString("kelas"));
            }rs.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"ERROR DIKelas");
        }
    }
    
