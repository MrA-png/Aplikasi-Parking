    public void filternis(KeyEvent a){
        if(Character.isAlphabetic(a.getKeyChar())){
            a.consume();
            JOptionPane.showMessageDialog(null,"Masukkan Angka","Informasi",JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void filterhurufbesar(KeyEvent b){
        if(Character.isLowerCase(b.getKeyChar())){
            b.consume();
            JOptionPane.showMessageDialog(null,"Hanya angka dan huruf besar","Infomasi",JOptionPane.ERROR_MESSAGE);
        }
    }
