    private void btnSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSimpanMouseClicked
        if(jNopol.getText().equals("")){
            JOptionPane.showMessageDialog(null,"NO Polisi harus diisi!","Informasi",JOptionPane.ERROR_MESSAGE);
        }else if(jMerk.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Merk harus diisi!","Informasi",JOptionPane.ERROR_MESSAGE);
        }else if(jNamken.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Nama Kendaraan harus diisi!","Informasi",JOptionPane.ERROR_MESSAGE);
        }else if(jWarna.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Warna harus diisi!","Informasi",JOptionPane.ERROR_MESSAGE);
        }else if(jNis.getText().equals("")){
            JOptionPane.showMessageDialog(null,"NIS harus diisi!","Informasi",JOptionPane.ERROR_MESSAGE);
        }else if(jJurusan.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Jurusan harus diisi!","Informasi",JOptionPane.ERROR_MESSAGE);
        }else{
            int opsi = JOptionPane.showConfirmDialog(null,"simpan data?","Konfirmasi",JOptionPane.YES_NO_OPTION);
            if(opsi==JOptionPane.YES_OPTION){
                input_data();
                reset_form();
            }else{
                JOptionPane.showConfirmDialog(null,"Data gagal disimpan","Informasi",JOptionPane.ERROR_MESSAGE);
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanMouseClicked

